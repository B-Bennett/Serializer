public class Song {
    String artist;
    String title;
    String genre;
    String date;
    String grammys;

    public String getArtist() {
        return artist;
    }

    public String getTitle() {
        return title;
    }

    public String getGenre() {
        return genre;
    }

    public String getDate() {
        return date;
    }

    public String getGrammys() {
        return grammys;
    }
}
